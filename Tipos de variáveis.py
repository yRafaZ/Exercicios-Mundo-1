#Esse é o tipo de variável "int" pois ela é um numero interiro.
idade = 14
print(type(idade))

#O nome dessa variável é "float" pois é um número decimal.
dinheiro = 0.50
print(type(dinheiro))

#Desta aqui o nome é "str" pois ela a variável é em letras e não em números.
#Para escrever a variável do tipo "str" é necessário colocar entre aspas "" o valor da variável caso ela seja do tipo "str" apenas.
nome = "Rafael"
print(type(nome))

#Essa se chama de "bool" quando ela é de verdairo ou falso.

homem = True
print(type(homem))

#Essa também é uma "str" por mais que tenha números e letras.
numeroeletras = input("Coloque uma frase com letras e números: ")
print(type(numeroeletras))